#include "../inc/cub.h"

int	find_path(char *str, int i)
{
	if (!str || !str[i])
		return (0);
	while (str[i] && (str[i] == ' ' || str[i] == '\n'
			|| (str[i] >= 9 && str[i] <= 13)))
				i++;
	return (i);
}
